package POJO;

import java.time.LocalDate;

/**
 *
 * @author Carmona
 */
public class EntregaActividad {
    private int idEntregaActividad;
    private int idActividad;
    private String descripcion;
    private int idUsuario;
    private LocalDate fechaEntrega = LocalDate.now();
    private byte[] archivo;
    private String nombreArchivo;
    private int calificacion;

    public int getIdEntregaActividad() {
        return idEntregaActividad;
    }

    public void setIdEntregaActividad(int idEntregaActividad) {
        this.idEntregaActividad = idEntregaActividad;
    }

    public int getIdActividad() {
        return idActividad;
    }

    public void setIdActividad(int idActividad) {
        this.idActividad = idActividad;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public LocalDate getFechaEntrega() {
        return fechaEntrega;
    }

    public void setFechaEntrega(LocalDate fechaEntrega) {
        this.fechaEntrega = fechaEntrega;
    }

    public byte[] getArchivo() {
        return archivo;
    }

    public void setArchivo(byte[] archivo) {
        this.archivo = archivo;
    }

    public String getNombreArchivo() {
        return nombreArchivo;
    }

    public void setNombreArchivo(String nombreArchivo) {
        this.nombreArchivo = nombreArchivo;
    }

    public int getCalificacion() {
        return calificacion;
    }

    public void setCalificacion(int calificacion) {
        this.calificacion = calificacion;
    }
}
